def norm_size_pdf(s):
    s = s.upper().strip().replace(' ', '')
    s = s.replace('LT', '').replace('ZR', 'R').replace('C', '').replace('X', '/')
    s = re.sub(r'R(\d+)', r'/\1', s)
    s = re.sub(r'/+', '/', s)
    return s.strip('/')

def norm_size_excel(s):
    s = s.upper().strip().replace(' ', '')
    s = s.replace('LT', '').replace('C', '').replace('X', '/')
    s = re.sub(r'/+', '/', s)
    return s.strip('/')

def norm_type(t):
    t = t.upper().strip()
    for k, v in [
        ('PREMIO COMFORT 6','COMPORT 6'),('PRIMIO ARZ1','PREMIO ARZ1'),
        ('PRIMIO SPORT 6','PREMIO SPORT 6'),('TERRNO ARV H/T','TERRANO ARV H/T'),
        ('ROCK TRACK R/T','ROCK TRAK R/T'),('TRANSITO ARZ 6-C','TRANSITO'),
        ('TRANSITO ARZ 6-M','TRANSITO'),('TRANSITO ARZ6-X','TRANSITO'),
        ('TRANSITO ARZ6-C','TRANSITO'),('TERRANO ARV H/T','TERRANO'),
        ('TERRANO H/T','TERRANO'),('TERRAMAX ARV PRO A/T','TERRAMAX'),
        ('TERRAMAX ARV A/T','TERRAMAX'),('ULTRA SPORT ARV7','ULTRA ARV7'),
    ]:
        if k in t: t = t.replace(k, v)
    return t

pdf_exact = {}
pdf_by_size = {}
for code, size, typ, srp in pdf:
    ns = norm_size_pdf(size)
    nt = norm_type(typ)
    pdf_exact[(ns, nt)] = (code, srp, typ)
    pdf_by_size.setdefault(ns, []).append((code, srp, typ, nt))

wb = openpyxl.load_workbook('ADZ-RUGGEd-PRO_INVENTORY-SYSTEM-1.xlsx', data_only=True)
ws = wb['ARIVO']
excel_items = []
for row in ws.iter_rows(min_row=10, values_only=True):
    sku = row[0]
    desc = row[3]
    if not sku or str(sku).startswith('#'): continue
    excel_items.append((str(sku).strip(), str(desc).strip() if desc else ''))

issues = []

for sku, desc in excel_items:
    # Split on ' - ' (space-dash-space) to avoid splitting inside type names like ARZ6-C
    m = desc.split(' - ', 1)
    if len(m) == 2:
        typ_raw, size_raw = m[0].strip(), m[1].strip()
    else:
        # Fallback: last whitespace-separated token is size
        tokens = desc.rsplit(None, 1)
        typ_raw = tokens[0].strip(' -') if len(tokens) == 2 else desc
        size_raw = tokens[1].strip(' -') if len(tokens) == 2 else ''

    ns = norm_size_excel(size_raw)
    nt = norm_type(typ_raw)
    key = (ns, nt)

    found = pdf_exact.get(key)
    if found:
        continue  # perfect match, skip

    # Size-only fallback
    candidates = pdf_by_size.get(ns, [])
    if candidates:
        best = sorted(candidates, key=lambda x: 0 if nt in x[3] or x[3] in nt else 1)
        pdf_code, srp, pdf_type, pdf_nt = best[0]
        issue_type = 'TYPE MISMATCH'
        note = f'Excel type: {nt} | PDF type: {norm_type(pdf_type)} | PDF code: {pdf_code} | PDF SRP: {srp}'
    else:
        issue_type = 'NOT IN PDF'
        pdf_code, srp, pdf_type = '-', '-', '-'
        note = 'Size not found in PDF price list at all'

    issues.append({
        'ADZ SKU': sku,
        'Excel Description': desc,
        'Issue': issue_type,
        'Excel Type': nt,
        'PDF Code': pdf_code,
        'PDF Type': pdf_type,
        'PDF SRP': srp,
        'Note': note,
    })

# Write CSV
out_path = 'scripts/arivo_issues.csv'
with open(out_path, 'w', newline='', encoding='utf-8') as f:
    fieldnames = ['ADZ SKU','Excel Description','Issue','Excel Type','PDF Code','PDF Type','PDF SRP','Note']
    w = csv.DictWriter(f, fieldnames=fieldnames)
    w.writeheader()
    w.writerows(issues)

print(f"Issues found: {len(issues)}")
print(f"Saved to: {out_path}")
